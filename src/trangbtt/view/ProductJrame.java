    public void viewData(List<ProductDTO> listPro) {
        tblProductModel.setRowCount(0);
        for (ProductDTO productDTO : listPro) {
            tblProductModel.addRow(productDTO.toVector());
        }
    }

    public void loadTypeID() {
        Connection conn = null;
        PreparedStatement preStm = null;
        ResultSet rs = null;
        try {
            String sql = "Select TypeID From Type";
            conn = MyConnection.getMyConnection();
            preStm = conn.prepareStatement(sql);
            rs = preStm.executeQuery();
            while (rs.next()) {
                String type = rs.getString("TypeID");
                cbxType.addItem(type);
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                try {
                    if (rs != null) {
                        rs.close();
                    }
                    if (preStm != null) {
                        preStm.close();
                    }
                    if (conn != null) {
                        conn.close();
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

  

