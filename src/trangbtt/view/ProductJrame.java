    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        String productID = txtID.getText();
        String name = txtName.getText();
        String price = txtPrice.getText();
        String typeID = (String) cbxType.getSelectedItem();
//        boolean delete = false;
        boolean valid = true;
        if (productID.length() == 0) {
            valid = false;
            JOptionPane.showMessageDialog(this, "ID can't be blank");
        }
         if (!productID.matches("^Pr[0-9]*$")) {
            valid = false;
            JOptionPane.showMessageDialog(this, "Input fomat ID Pr + digit");
        }
        
        if (name.length() == 0) {
            valid = false;
            JOptionPane.showMessageDialog(this, "Name can't be blank");
        }
        if (price.length() == 0) {
            valid = false;
            JOptionPane.showMessageDialog(this, "Price can't be blank");
        }
        int priceI = 0;
        if (!price.matches("^[0-9]*$")) {
            valid = false;
            JOptionPane.showMessageDialog(this, "Input Number in Price");
        }
        if (valid) {
            priceI = Integer.parseInt(price);
            ProductDTO dto = new ProductDTO(productID, name, priceI, typeID);
            ProductDAO dao = new ProductDAO();
            try {
                boolean check = dao.insert(dto);
                if (check) {
                    JOptionPane.showMessageDialog(this, "Insert success");
                    tblProductModel.addRow(dto.toVector());
                } else {
                    JOptionPane.showMessageDialog(this, "Insert failed");
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());

            }
        }

    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String productID = txtID.getText();
        String name = txtName.getText();
        String price = txtPrice.getText();
        String typeID = (String) cbxType.getSelectedItem();
        boolean valid = true;
        if (productID.length() == 0) {
            valid = false;
            JOptionPane.showMessageDialog(this, "ID can't be blank");
        }
        if (!productID.matches("^Pr[0-9]*$")) {
            valid = false;
            JOptionPane.showMessageDialog(this, "Input fomat ID Pr + digit");
        }
        if (name.length() == 0) {
            valid = false;
            JOptionPane.showMessageDialog(this, "Name can't be blank");
        }
        if (price.length() == 0) {
            valid = false;
            JOptionPane.showMessageDialog(this, "Price can't be blank");
        }
        int priceI = 0;
        if (!price.matches("^[0-9]*$")) {
            valid = false;
            JOptionPane.showMessageDialog(this, "Input Number in Price");
        }
        if (valid) {
            priceI = Integer.parseInt(price);
            ProductDTO dto = new ProductDTO(productID, name, priceI, typeID);
            ProductDAO dao = new ProductDAO();
            try {
                if (dao.update(dto)) {
                    JOptionPane.showMessageDialog(this, "Update success!");
                    int row = tblProductView.getSelectedRow();
                    // thay doi cai nao chon cai do
                    tblProductModel.setValueAt(dto.getName(), row, 1);
                    tblProductModel.setValueAt(dto.getPrice(), row, 2);
                    tblProductModel.setValueAt(dto.getTypeID(), row, 3);
                } else {
                    JOptionPane.showMessageDialog(this, "Update Failed!");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void tblProductViewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductViewMouseClicked
        int row = tblProductView.getSelectedRow();
        String id = (String) tblProductModel.getValueAt(row, 0);
        try {
            ProductDAO dao = new ProductDAO();
            ProductDTO dto = dao.findByPrimaryKey(id);
            txtID.setText(dto.getProductID());
            txtName.setText(dto.getName());
            txtPrice.setText(dto.getPrice() + "");
            cbxType.setSelectedItem(dto.getTypeID());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_tblProductViewMouseClicked

