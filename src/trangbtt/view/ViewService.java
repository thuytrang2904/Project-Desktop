    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        ViewSerDAO dao = new ViewSerDAO();
        String id = (String)cbxID.getSelectedItem();
        boolean valid = true;
        
        data = dao.view(id);
        if (data.size() == 0) {
            valid = false;
            JOptionPane.showMessageDialog(this, "Not found");
        }
        if (valid) {
            txtDate.setText(dao.date(id));
            txtOwner.setText(dao.phone(id));
            txtNameOw.setText(dao.name(id));
            int sum = 0;
            Vector total = new Vector();
            data.add(total);
            Vector fax = new Vector();
            data.add(fax);


            DefaultTableModel model = new DefaultTableModel(data, header);
            tblView.setModel(model);
            for (int i = 0; i < tblView.getRowCount() - 2; i++) {
                Vector row1 = (Vector) data.get(i);
                sum += (int) row1.get(2);
            }
            total.add(1, "Total:");
            total.add(2, sum);

            double t = sum - (sum * 0.8);
            fax.add(1, "Fax(8%):");
            fax.add(2, t);

            txtMoney.setText(sum - t + "");
        }
    }//GEN-LAST:event_btnViewActionPerformed

