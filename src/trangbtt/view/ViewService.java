    void uploadID() {
        Connection cn = null;
        PreparedStatement pre = null;
        ResultSet rs = null;

        try {

            String sql = "select DISTINCT id.InvoiceIDS from Invoice_Ser_Detail id, Invoice_Ser ins where id.InvoiceIDS = ins.InvoiceIDS";
            cn = MyConnection.getMyConnection();
            pre = cn.prepareStatement(sql);
            rs = pre.executeQuery();

            while (rs.next()) {
                String id = rs.getString("InvoiceIDS");
                cbxID.addItem(id);
            }

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                try {
                    if (rs != null) {
                        rs.close();
                    }
                    if (pre != null) {
                        pre.close();
                    }
                    if (cn != null) {
                        cn.close();
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }
